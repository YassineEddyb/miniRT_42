#include "libft.h"

char	*ft_strdup(char *src)
{
	char	*p;
	int		len;
	int		i;

	len = ft_strlen(src);
	p = malloc (len * sizeof (char) + 1);
	if (p == NULL)
		return (NULL);
	i = 0;
	while (src[i] != '\0')
	{
		p[i] = src[i];
		i++;
	}
	p[i] = '\0';
	return (p);
}
